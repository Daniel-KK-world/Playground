class Calculator:
    def __init__(self):
        self.num1 = input("Please enter num1: ")
        self.num2 = input("Please enter num2: ")
    
    def add_numbers(self):
        print(self.num1 + self.num2)
        
    
    def subtract_numbers(self):
        print(self.num1 - self.num2) 
         
    
    def multiply_numbers(self):
        print(self.num1 * self.num2)